package com.example.model;

//部门
public class Post {
    //部门ID
    private String id;
    //部门名称
    private String postName;

    public String getId() {
        return id;
    }
    public String getPostName() {
        return postName;
    }

    public void setId(String id) {
        this.id = id;
    }
    public void setPostName(String postName) {
        this.postName = postName;
    }
}
